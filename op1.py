x = input("Enter your age: ")
age = float(x)
if age < 1 :
    print("Amazing")
elif age < 5:
    print("You are a baby.")
    print("Stay with Parents")
elif age < 10 :
    print("You are a child")
    print("Still Learning")
elif age < 16 :
    print("You are a teenager") 
    print("Find good friends")
elif age < 23 :
    print("WORK HARD")
elif age > 33 :
    print ("How are you?")
elif age > 100:
    print("Wonderful")    
else :
    print ("have a nice day!")              
